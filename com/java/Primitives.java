package com.java;

public class Primitives {
    public static void main(String[] args) {
        //stores a value of type integer
        int a=10;
        //stores a value of type charcter
        char letter='A';
        //Stores a value of type decimal.
        float c=12.345f;
        //'f' is used to declare that the number is of type float

        //double is used to store large number of decimal number.
        double num=1233456.8475656;

        //Long is used to store a large interger value
        long d=456754433434554354l;

        //boolean is used only to store value is true of false
        boolean active=true;

        System.out.println("These are some of the primitive data types :");
        System.out.println("Integer is : "+a);
        System.out.println("Long is : "+d);
        System.out.println("Float is : "+c);
        System.out.println("double is : "+num);
        System.out.println("charcter is : "+letter);
        System.out.println("boolean is : "+active);
    }
}
